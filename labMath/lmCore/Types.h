#ifndef __TYPES_H_
#define __TYPES_H_

//--------------------------------------------------------------------------------//
// Unsinged Number Types
//--------------------------------------------------------------------------------//
typedef unsigned char		UI8;
typedef unsigned short		UI16;
typedef unsigned int		UI32;
typedef unsigned long long	UI64;

//--------------------------------------------------------------------------------//
// Signed Number Types
//--------------------------------------------------------------------------------//
typedef signed char			I8;
typedef signed short		I16;
typedef signed int			I32;
typedef signed long long	I64;

//--------------------------------------------------------------------------------//
// Floating poI32 types
//--------------------------------------------------------------------------------//
typedef float				F32;
typedef double				D64;

//--------------------------------------------------------------------------------//
// Pi Values
//--------------------------------------------------------------------------------//
const D64 PI_HALF		= 1.57079632679489661;
const D64 PI			= 3.14159265358979323;
const D64 PI_TWO		= 6.28318530717958646;

#endif//__TYPES_H_
